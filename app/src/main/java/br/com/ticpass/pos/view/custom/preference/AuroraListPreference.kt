package br.com.ticpass.pos.view.custom.preference

import android.content.Context
import android.util.AttributeSet
import androidx.preference.ListPreference

class AuroraListPreference : ListPreference {

    constructor(context: Context) : super(context)

    constructor(context: Context, attrs: AttributeSet?) : super(context, attrs)

    constructor(context: Context, attrs: AttributeSet?, defStyleAttr: Int) : super(
        context,
        attrs,
        defStyleAttr
    )

    constructor(
        context: Context,
        attrs: AttributeSet?,
        defStyleAttr: Int,
        defStyleRes: Int
    ) : super(context, attrs, defStyleAttr, defStyleRes)

    override fun getPersistedString(defaultReturnValue: String?): String {
        return getPersistedInt(defaultReturnValue?.toInt() ?: -1).toString()
    }

    override fun persistString(value: String?): Boolean {
        return if (value != null) persistInt(Integer.valueOf(value)) else false
    }
}
